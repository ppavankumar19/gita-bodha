export const getAllThemes = () => {
  const themes = new Set();
  slokas.forEach(s => s.moral_themes.forEach(t => themes.add(t)));
  return Array.from(themes).sort();
};

export const getAllChapters = () => {
  const chapters = new Set(slokas.map(s => s.chapter));
  return Array.from(chapters).sort((a, b) => a - b);
};

export const getSlokaById = (id) => slokas.find(s => s.id === id) || null;

export const getFeaturedSlokas = () => slokas.filter(s => s.is_featured);

export const getDailySloka = () => {
  const dayOfYear = Math.floor((Date.now() - new Date(new Date().getFullYear(), 0, 0)) / 86400000);
  return slokas[dayOfYear % slokas.length];
};
